    public void startGame() {

        btnNumber = new JButton[]{jButton1, jButton2, jButton3, jButton4, jButton5, jButton6, jButton7, jButton8, jButton9,
            jButton10, jButton11, jButton12, jButton13, jButton14, jButton15, jButton16, jButton17, jButton18, jButton19,
            jButton20, jButton21, jButton22, jButton23, jButton24, jButton25};

        for (JButton b : btnNumber) {
            b.setEnabled(false);
        }

        Random rnd = new Random();

        arrButton25 = new ArrayList<>();

        int i = 0;
        while (i < 25) {
            int temp = rnd.nextInt(25) + 1;
            if (arrButton25.contains(temp)) {
                continue;
            }

            arrButton25.add(temp);
            i++;
        }

        arrButton50 = new ArrayList<>();

        int j = 0;
        while (j < 25) {
            int temp = rnd.nextInt(25) + 26;
            if (arrButton50.contains(temp)) {
                continue;
            }

            arrButton50.add(temp);
            j++;
        }
        for (int k = 0; k < 25; k++) {
            btnNumber[k].setText((arrButton25.get(k)) + "");
        }
        for (int x = 0; x < 50; x++) {
            arrCorrectOrder.add(x + 1);
        }
        System.out.println(arrButton25);
        System.out.println(arrButton50);
        System.out.println(arrCorrectOrder);

    }

