    private void jButtonStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonStartActionPerformed
        if ("Start Game".equals(jButtonStart.getText())) {
            isPaused = false;
            Timer.startTimer();
            jButtonStart.setText("Pause Game");

            for (JButton b : btnNumber) {
                b.setEnabled(true);
            }

        } else {
            isPaused = true;
            Timer.startTimer();
            jButtonStart.setText("Start Game");

            for (JButton b : btnNumber) {
                b.setEnabled(false);
            }

        }
    }//GEN-LAST:event_jButtonStartActionPerformed

    private void jButtonResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonResetActionPerformed
        isPaused = true;
        jSB.setVisible(false);
        jButtonStart.setEnabled(true);
        mistakeCount = 0;
        jLabelMistakeCount.setText("Mistakes: 0");
        resetGame = true;
        Timer.startTimer();
        arrCorrectOrder.clear();
        
        if (!btnNumber[23].isVisible()) {
            for (JButton b : btnNumber) {
                b.setVisible(true);
            }
        }
        startGame();
        jButtonStart.setText("Start Game");
    }//GEN-LAST:event_jButtonResetActionPerformed

